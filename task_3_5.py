from random import randint


def get_jokes(num):             # Объявляем функцию get_jokes
    list_jokes = []             # Объявляем пустой список шуток list_jokes
    for i in range(num):        # Генерируем кол-во запрошенных шуток и помещаем их в список list_jokes
        list_jokes += [f"{nouns[randint(0, 4)]} {adverbs[randint(0, 4)]} {adjectives[randint(0, 4)]}"]
    print(list_jokes)           # Печатаем список шуток


nouns = ["автомобиль", "лес", "огонь", "город", "дом"]
adverbs = ["сегодня", "вчера", "завтра", "позавчера", "ночью"]
adjectives = ["веселый", "яркий", "зеленый", "утопичный", "мягкий"]
while True:
    number = input('Введите количество шуток, или нажмите Enter для выхода')
    if number == "":
        break
    elif number.isdigit():
        get_jokes(int(number))
