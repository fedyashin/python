# Примечание:
# - Задача обычной сложности: создайте новый список и заполните его нужными значениями, включая элементы-кавычки,
# например: ['в', '"', '05', '"', 'часов',...]
# или измените существующий список, но не добавляйте новые элементы-кавычки, кавычки сразу внесите в элемент-число,
